#Loops are used to repeat instructions.
count=1 #here count is an iterator(variable used to access the loop)
while(count<=5):
   print("hello ",count)
   count+=1 #assignment operator
print("Loop ended")   

#loop in reverse order
i=5
while(i>=1):
   print("hello ",i)
   i-=1
print("Loop ended")   

   