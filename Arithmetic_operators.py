#operators
"""operators is used to perform mathmatical or logical 
   operation on values.
   here, values are called as operands."""

#arithmetic operator

#addition(+)
a=5+7
str="hello"+"world"  #concatenate strings
print("addition",a,str)

#sub
a=7-5
print("sub",a)

#multiplication
a=6*5
str="hello"*2
print(a,str)

#division
a=7/5
print(a)  #floating number

#floor division
a=7//5
print(a)  #integer number

#modular division
a=7%5
print(a)   #gives remainder

#exponential 
a=7**2
print(a)



