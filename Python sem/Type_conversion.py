#Type conversion
"""the conversion of value of one data type into another data type is called type conversion
   two types--implicit type conversion
            --explicit type conversion"""
#implicit type conversion
"""python automatically converts one data type into another
   no user involvement"""
a=5
b=5.4
print(a+b)
#explicit type conversion
"""users convert the data type into required data type"""
a=4
b="5"
sum=a+int(b)
print(sum)
print(type(5))