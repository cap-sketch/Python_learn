#assignment operator
"""used to assign values"""

num1=5  #assign value of right to left
print(num1)
num1+=5 #add value of left operand and right and store in left operand.
print(num1)
num1-=5 #sub value of left operand to right and store in left operand.
print(num1)
num1*=5
print(num1)
num1/=5
print(num1)
num1//=5
print(num1)
num1%=5
print(num1)
num1**=5
print(int(num1))
