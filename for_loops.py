#for loops--are used for sequential traversal
#         --for traversing list,string,tuples,etc.

#for in list
list=[1,2,3]
for i in list:    # i is any variable
    print(i)      #in is a special operator
print("loop ended\n")

#for in tuple
tuple=(1,2,3,4,5)
for i in tuple:
    print(i)
print("loop ended\n")    


#for in string
String="hello"
for s in String:
    print(s)