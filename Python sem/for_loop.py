#for loop
"""it iterates over a sequence(such as list,tuple,string or range)or any other iterable object."""
#for loop with list
fruits=["mango","apple","guava","grapes"]
for i in fruits:
    print(i)
#for loop with string
word="python"
for letter in word:
    print(letter)
#for loop with range
for i in range(1,6):
    print(f"the value is {i}")